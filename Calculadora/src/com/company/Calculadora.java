package com.company;

public class Calculadora {

    public float soma(float x, float y) {
        return x + y;
    }
    public float sub (float x, float y) {
        return x - y;
    }
    public float mult(float x, float y) {
        return x * y;
    }
    public float div(float x, float y) {
        return x / y;
    }
}
